public class Carretera {
    private int inicioX;
    private int finalX;
    private int inicioY;
    private int finalY;
    private boolean esVertical;

    public Carretera(int inicioX, int finalX, int inicioY, int finalY, boolean esVertical) {
        this.inicioX = inicioX;
        this.finalX = finalX;
        this.inicioY = inicioY;
        this.finalY = finalY;
        this.esVertical = esVertical;
    }
    
    
    
}
